void MonoGfx::draw_char(int x, int y, char c, bool on) {
    if (c < 32 || c > 127) c = '?';
    const uint8_t* g = font5x7[c - 32];
    for (int col = 0; col < 5; ++col) {
        uint8_t bits = g[col];
        for (int row = 0; row < 7; ++row) {
            if ((bits >> row) & 1u) pixel(x + col, y + row, on);
        }
    }
}

void MonoGfx::text(int x, int y, const std::string& s, bool on) {
    int cx = x;
    for (char c : s) {
        draw_char(cx, y, c, on);
        cx += 6;
        if (cx >= w_) break;
    }
}
